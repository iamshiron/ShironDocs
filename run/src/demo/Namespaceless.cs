#pragma warning disable CA1050 // Declare types in namespaces

/// <summary>
/// A class that wanders the global scope, unburdened by the shackles of a namespace.
/// </summary>
/// <remarks>
/// Is it a bug? Is it a feature? No, it's a statement against the organized society of code.
/// </remarks>
public class NamespacelessClass {
    /// <summary>
    /// Does something, somewhere, somehow.
    /// </summary>
    /// <remarks>
    /// The universe doesn't always need to know where you belong to let you function.
    /// </remarks>
    public void DoSomething() {
        Console.WriteLine("Hello from a namespaceless class!");
    }
}
